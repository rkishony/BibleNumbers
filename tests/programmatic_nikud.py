def test_preprocess_token():
    s = 'וּשְׁלֹשִׁים'
    assert preprocess_token(s)[0] == 'שְׁלֹשִׁים'

    s = 'בִּשְׁלוֹשָׁה'
    assert preprocess_token(s)[0] == 'שְׁלוֹשָׁה'


